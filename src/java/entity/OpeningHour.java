package entity;

/**
 *
 * @author kynhanht
 */
public class OpeningHour {
    private String day,time;

    public OpeningHour() {
    }

    public OpeningHour(String day, String time) {
        this.day = day;
        this.time = time;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    
}
